async function connectWallet() {
  if (typeof window.ethereum !== "undefined") {
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    contract = new ethers.Contract(contractAddress, abi, signer);
    try {
      const accounts = await window.ethereum.request({
        method: "eth_requestAccounts",
      });
      console.log(accounts);
      if (accounts) {
        document.getElementById("walletStatus").textContent = "Connected";
        document.getElementById("walletStatus").className = "connected";
      } else {
        document.getElementById("walletStatus").textContent = "Disconnected";
        document.getElementById("walletStatus").className = "disconnected";
      }
    } catch (error) {
      console.error("User denied account access");
    }
  } else {
    alert("Please install MetaMask!");
  }
}

async function publicSaleTransfer() {
  const amount = document.getElementById("publicSaleAmount").value;
  const weiAmount = ethers.utils.parseEther(amount);
  try {
    const tx = await contract.publicSaletransfer({ value: weiAmount });
    await tx.wait();
    alert("Transaction successful!");
  } catch (error) {
    console.error("Error:", error);
  }
}

